    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
         
        int Radio=Integer.parseInt(P3X.getText());
        int grados=Integer.parseInt(P2X.getText());
        int minutos=Integer.parseInt(P2X1.getText());
        int segundos=Integer.parseInt(P2X2.getText());
        int n1=Integer.parseInt(N1.getText());
        int n2=Integer.parseInt(N2.getText());
        int e1=Integer.parseInt(E1.getText());
        int e2=Integer.parseInt(E2.getText());
        double gradocurva = 572.96/Radio;
        double decimal = grados + (minutos / 60.0) + (segundos / 3600.0);
        double deltaRadianes = Math.toRadians(decimal);
        double anguloMitad = deltaRadianes / 2;
        double tangente = Radio*Math.tan(anguloMitad);
        double cuerdaPrincipal = 2*Radio*Math.sin(anguloMitad);
        double longituddecurva =(deltaRadianes/gradocurva)*10;
        double flecha = Radio * (1 - Math.cos(anguloMitad));
        double externa = Radio * ((1/Math.cos(anguloMitad))-1 );
        double distaciahoriz = sqrt(pow((n2 - n1), 2)+pow((e2 - e1), 2));
        double entretangente = distaciahoriz - tangente;
        double deflexionpara10m = gradocurva/2;
        double deflexionpara1m = deflexionpara10m/2;
        
        P1X.setText(String.valueOf(gradocurva));
        txt_TANGENTE1.setText(String.valueOf(tangente));
        txt_CUERDAPRINCIPAL.setText(String.valueOf(cuerdaPrincipal));
        txt_LONGITUDDECURVA.setText(String.valueOf(longituddecurva));
        txt_FLECHA.setText(String.valueOf(flecha));
        txt_EXTERNA.setText(String.valueOf(externa));
        txt_DISTANCIAHORIZ.setText(String.valueOf(distaciahoriz));
        txt_ENTRETANGENTE.setText(String.valueOf(entretangente));
        txt_DEFLEXIONPARA10m.setText(String.valueOf(deflexionpara10m));
        txt_DEFLEXIONPARA1m.setText(String.valueOf(deflexionpara1m));
        
         CurvaCircularPanel curvaCircularPanel = new CurvaCircularPanel(Radio, anguloMitad);

    SALIDA.setLayout(new FlowLayout()); // Establecer el administrador de diseño

    SALIDA.removeAll();
    SALIDA.add(curvaCircularPanel);

    // Establecer el tamaño del panel CurvaCircularPanel dentro de SALIDA
    curvaCircularPanel.setPreferredSize(new Dimension(400, 400));

    SALIDA.revalidate();
    SALIDA.repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

