    public class CurvaCircularPanel extends JPanel {
    private int radio;
    private double anguloMitad;

    public CurvaCircularPanel(int radio, double anguloMitad) {
        this.radio = radio;
        this.anguloMitad = anguloMitad;

        setPreferredSize(new Dimension(400, 400));
    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);

        Graphics2D g2d = (Graphics2D) g;
        g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

        int centroX = getWidth() / 2;
        int centroY = getHeight() / 2;
        int diametro = radio * 2;

        int inicioAngulo = 90 - (int) Math.toDegrees(anguloMitad);
        int arcoAngulo = (int) Math.toDegrees(anguloMitad) * 2;

        g2d.setColor(Color.BLACK);
        g2d.drawArc(centroX - radio, centroY - radio, diametro, diametro, inicioAngulo, arcoAngulo);
    }
}
